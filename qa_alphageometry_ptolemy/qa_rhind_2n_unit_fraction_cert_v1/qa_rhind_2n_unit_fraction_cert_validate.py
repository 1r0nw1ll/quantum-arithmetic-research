assert len(RHIND) == 50, f"Expected 50 entries, got {len(RHIND)}"


def digital_root(n: int) -> int:
    return ((n - 1) % 9) + 1


def rhind_sum(n: int) -> Fraction:
    return sum(Fraction(1, d) for d in RHIND[n])


checks = {}
passed = 0
failed = 0

# C1 -- Exact arithmetic: all 50 entries sum to exactly 2/n as Fraction
failures_c1 = [
    n for n in RHIND
    if rhind_sum(n) != Fraction(2, n)
]
ok_c1 = len(failures_c1) == 0 and len(RHIND) == 50
checks["C1_exact_fraction_all_50_entries"] = "PASS" if ok_c1 else "FAIL"
if ok_c1: passed += 1
else:     failed += 1

# C2 -- Satellite 2-term: all n with 3|n have exactly 2 unit fractions
satellite_n = [n for n in sorted(RHIND) if n % 3 == 0]
cosmos_n    = [n for n in sorted(RHIND) if n % 3 != 0]
ok_c2 = (
    len(satellite_n) == 17
    and all(
        orbit_family(digital_root(n), digital_root(n), 9) in ("satellite", "singularity")
        for n in satellite_n
    )
    and all(len(RHIND[n]) == 2 for n in satellite_n)
)
checks["C2_satellite_2term_17_entries"] = "PASS" if ok_c2 else "FAIL"
if ok_c2: passed += 1
else:     failed += 1

# C3 -- 3-divisor formula: Rhind denominators for n=3k equal (2k, 6k) exactly
formula_failures = []
for n in satellite_n:
    k = n // 3
    expected = (2 * k, 6 * k)
    if RHIND[n] != expected:
        formula_failures.append((n, RHIND[n], expected))
ok_c3 = len(formula_failures) == 0
checks["C3_3divisor_formula_2over3k_eq_1over2k_plus_1over6k"] = "PASS" if ok_c3 else "FAIL"
if ok_c3: passed += 1
else:     failed += 1

# C4 -- 4-term decompositions appear only in Cosmos; exactly 8 such n
