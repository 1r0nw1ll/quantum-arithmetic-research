four_term_in_satellite = [n for n in four_term_n if n % 3 == 0]
ok_c4 = (
    len(four_term_n) == 8
    and four_term_in_satellite == []
    and all(orbit_family(digital_root(n), digital_root(n), 9) == "cosmos"
            for n in four_term_n)
    and sorted(four_term_n) == [29, 43, 61, 73, 79, 83, 89, 101]
)
checks["C4_4term_cosmos_only_8_entries_29_43_61_73_79_83_89_101"] = "PASS" if ok_c4 else "FAIL"
if ok_c4: passed += 1
else:     failed += 1

# C5 -- Theorem NT: all denominators are integers; no float; orbit_family
#        returns strings (discrete); Fraction arithmetic never touches float
denom_types_ok = all(
    isinstance(d, int) and d > 0
    for n in RHIND for d in RHIND[n]
)
orbit_types_ok = all(
    isinstance(orbit_family(digital_root(n), digital_root(n), 9), str)
    for n in RHIND
)
ok_c5 = denom_types_ok and orbit_types_ok and all(
    isinstance(rhind_sum(n), Fraction) for n in RHIND
)
checks["C5_theorem_nt_all_denominators_int_orbit_str_sum_fraction"] = "PASS" if ok_c5 else "FAIL"
if ok_c5: passed += 1
else:     failed += 1

