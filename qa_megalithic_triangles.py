from math import gcd, sqrt
from fractions import Fraction


def qa_direction_from_triple(a_side, b_side, c_hyp):
    """Given a Pythagorean triple (a, b, c) with a²+b²=c², find the QA direction.

    A primitive Pythagorean triple can be written as:
      (m²-n², 2mn, m²+n²)  for coprime m>n, m-n odd
    OR equivalently in QA:
      (F, C, G) = (d²-e², 2de, d²+e²) = (ab, 2de, d²+e²)

    So m=d, n=e (the QA direction).
    """
    # Check which ordering gives a valid (m,n) pair
    # The even side is always 2mn = C (green quadrance)
    if a_side % 2 == 0:
        C, F = a_side, b_side
    elif b_side % 2 == 0:
        C, F = b_side, a_side
    else:
        # Both odd — might be a scaled triple
        g = gcd(gcd(a_side, b_side), c_hyp)
        return qa_direction_from_triple(a_side // g, b_side // g, c_hyp // g) if g > 1 else None

    G = c_hyp

    # C = 2de, F = d²-e² = ab, G = d²+e²
    # From C and G: d² = (G+F)/2, e² = (G-F)/2
    d_sq = (G + F) // 2 if (G + F) % 2 == 0 else None
    e_sq = (G - F) // 2 if (G - F) % 2 == 0 else None

    if d_sq is None or e_sq is None:
        return None

    d = round(sqrt(d_sq))
    e = round(sqrt(e_sq))

    if d * d != d_sq or e * e != e_sq:
        return None

    b = d - e
    a = d + e  # = b + 2e

    # Verify
    if 2 * d * e != C:
        return None
    if d * d + e * e != G:
        return None

    return (b, e, d, a, C, F, G)


